
public class Operators {
	public static void main(String args[]) 
	{
		int x=10;
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);
		int a=10;
		int b=6;
		boolean c=true;
		boolean d=false;
		System.out.println(~a); 
		System.out.println(~b); 
		System.out.println(!c);
		System.out.println(!d);
		int e=10;
		int f=5;
		System.out.println(e+f); 
		System.out.println(e-f);
		System.out.println(e*f);
		System.out.println(e/f);
		System.out.println(e%f);
	   } 


	}
