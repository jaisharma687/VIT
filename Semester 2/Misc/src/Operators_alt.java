
public class Operators_alt {

	public static void main(String[] args) {
		int h = 10;
		int g = 20;
		System.out.println(h>g);
		System.out.println(h<g);
		System.out.println(h>=g);
		System.out.println(h<=g);
		System.out.println(h==g);
		System.out.println(h!=g);
		int a = 10; 
		int b = 20; 
		System.out.println(a&b); 
		System.out.println(a|b); 
		System.out.println(~a); 
		System.out.println(a<<2);
		System.out.println(a>>2);
		System.out.println(a>>>2);
	}

}
